
class FoodItem:

    def __int__(self, ndbNo, name, group, measurement, calories, CF, FF, PF, SF,):
        self.ndbNo = ndbNo
        self.name = name
        self.group = group
        self.measurement = measurement
        self.calories = calories
        self.carbs = CF
        self.fats = FF
        self.proteins = PF
        self.sugars = SF





